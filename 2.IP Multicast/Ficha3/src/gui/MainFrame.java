    private void JloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JloginActionPerformed
        ip = this.Jip.getText();
        port = this.Jport.getText();
        name = this.Jname.getText();
        if (name.isEmpty() || ip.isEmpty() || port.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Campos obrigat√≥rios: IP, Port, Name", "Alert", JOptionPane.WARNING_MESSAGE);
        } else {
            this.textarea.append("Connected to " + ip + "...\n");
            this.Jsend.setEnabled(true);
            this.Jlogin.setEnabled(false);
            this.Jlogout.setEnabled(true);
            this.Jip.setEnabled(false);
            this.Jport.setEnabled(false);
            this.Jname.setEnabled(false);
            rm.login(ip, Integer.parseInt(port), textarea);
        }
    }//GEN-LAST:event_JloginActionPerformed

